    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     
        String a= jTextField1.getText();
        switch(a){
            case "amarillo" -> {
                dialog.getContentPane().setBackground(new Color(255,255,51));
                dialog.setVisible(true);
            }
            case "azul" -> {
                dialog.getContentPane().setBackground(new Color(0,0,153));
                dialog.setVisible(true);
            }
            case "blanco" -> {
                dialog.getContentPane().setBackground(new Color(255,255,255));
                dialog.setVisible(true);
            }
            case "gris" -> {
                dialog.getContentPane().setBackground(new Color(160,160,160));
                dialog.setVisible(true);
            }
            case "morado" -> {
                dialog.getContentPane().setBackground(new Color(153,0,153));
                dialog.setVisible(true);
                
            }
            case "negro" -> {
                dialog.getContentPane().setBackground(new Color(0,0,0));
                dialog.setVisible(true);
            }
            case "rojo" -> {
                dialog.getContentPane().setBackground(new Color(204,0,0));
                dialog.setVisible(true);
            }
            case "rosa" -> {
                dialog.getContentPane().setBackground(new Color(255,153,255));
                dialog.setVisible(true);
            }
            case "verde" -> {
                dialog.getContentPane().setBackground(new Color(0,254,0));
                dialog.setVisible(true);
            }
            default -> 
                respuestaIncorrecta();
            }
        if(jTextField1.getText().trim().length()==0){
            JOptionPane.showMessageDialog(this, "No ingresaste ningún color.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

