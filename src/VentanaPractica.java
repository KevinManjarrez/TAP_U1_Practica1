    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseClicked
        if(jList1.getSelectedIndex()>0){
            JOptionPane.showMessageDialog(this,"Elegiste el color: "+ jList1.getSelectedValue());
            jTextField1.setText(jList1.getSelectedValue().toLowerCase());
        }else{
            JOptionPane.showMessageDialog(this,"Selecciona algún color");
        }
    }//GEN-LAST:event_jList1MouseClicked

